export function getCampaignDailyGoals(
  difficultyId: CampaignDifficultyId,
  dayNumber: number
): DayGoal[] {
  const goalsByDay = campaignDailyGoalsByDifficulty[difficultyId];
  const safeDayIndex = Math.max(0, Math.floor(dayNumber) - 1);
  const fallbackGoals = goalsByDay[goalsByDay.length - 1] ?? [];
  const resolvedGoals = goalsByDay[safeDayIndex] ?? fallbackGoals;

  return resolvedGoals.map((goal) => ({ ...goal }));
}
